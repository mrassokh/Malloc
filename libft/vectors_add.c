#include "vector.h"

void		vectors_add(t_vector *res, t_vector *a, t_vector *b)
{
	if (!res || !a || !b)
		return ;
	res->x = a->x + b->x;
	res->y = a->y + b->y;
	res->z = a->z + b->z;
}
