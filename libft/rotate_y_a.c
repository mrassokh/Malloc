#include "matrix_mult.h"

int		rotate_y_a(t_matr *res, float fi)
{
	if (!res)
		return (0);
	res->mt[0][0] = cos(fi);
	res->mt[0][2] = sin(fi);
	res->mt[2][0] = -sin(fi);
	res->mt[2][2] = cos(fi);
	return (1);
}
