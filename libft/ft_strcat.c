#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	len;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	while (s2[i++])
		s1[len + i - 1] = s2[i - 1];
	s1[len + i - 1] = '\0';
	return (s1);
}
