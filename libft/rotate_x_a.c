#include "matrix_mult.h"

int		rotate_x_a(t_matr *res, float fi)
{
	if (!res)
		return (0);
	res->mt[1][1] = cos(fi);
	res->mt[1][2] = -sin(fi);
	res->mt[2][1] = sin(fi);
	res->mt[2][2] = cos(fi);
	return (1);
}
